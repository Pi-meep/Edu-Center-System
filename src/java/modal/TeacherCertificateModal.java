package modal;

import java.time.LocalDateTime;

/**
 *
 * @author Astersa
 */
public class TeacherCertificateModal {
        private Integer id;

    private Integer teacherId;
    private String imageURL;
    private String certificateName;
    private LocalDateTime issuedDate;
    private LocalDateTime createdAt;

    public TeacherCertificateModal() {
    }

    public TeacherCertificateModal(Integer id, Integer teacherId, String imageURL, String certificateName, LocalDateTime issuedDate, LocalDateTime createdAt) {
        this.id = id;
        this.teacherId = teacherId;
        this.imageURL = imageURL;
        this.certificateName = certificateName;
        this.issuedDate = issuedDate;
        this.createdAt = createdAt;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getTeacherId() {
        return teacherId;
    }

    public void setTeacherId(Integer teacherId) {
        this.teacherId = teacherId;
    }

    public String getImageURL() {
        return imageURL;
    }

    public void setImageURL(String imageURL) {
        this.imageURL = imageURL;
    }

    public String getCertificateName() {
        return certificateName;
    }

    public void setCertificateName(String certificateName) {
        this.certificateName = certificateName;
    }

    public LocalDateTime getIssuedDate() {
        return issuedDate;
    }

    public void setIssuedDate(LocalDateTime issuedDate) {
        this.issuedDate = issuedDate;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }
    
}
